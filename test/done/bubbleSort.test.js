import { expect } from "chai";
import bubbleSort from "../../practice/sort/bubblesort";

describe("bubbleSort", () => {
  it("", () => {
    expect(bubbleSort([5, 4, 3, 2, 1])).to.eql([1, 2, 3, 4, 5]);
  });
  // [ 5, 4, 3, 2, 1 ] 5 4
  // [ 4, 5, 3, 2, 1 ] 5 3
  // [ 4, 3, 5, 2, 1 ] 5 2
  // [ 4, 3, 2, 5, 1 ] 5 1
  // [ 4, 3, 2, 1, 5 ] 4 3
  // [ 3, 4, 2, 1, 5 ] 4 2
  // [ 3, 2, 4, 1, 5 ] 4 1
  // [ 3, 2, 1, 4, 5 ] 3 2
  // [ 2, 3, 1, 4, 5 ] 3 1
  // [ 2, 1, 3, 4, 5 ] 2 1

  it("", () => {
    expect(bubbleSort([8, 2, 3, 4, 5])).to.eql([2, 3, 4, 5, 8]);
  });
  // [ 8, 2, 3, 4 ] 8 2
  // [ 2, 8, 3, 4 ] 8 3
  // [ 2, 3, 8, 4 ] 8 4
  // [ 2, 3, 4, 8 ] 2 3
  // [ 2, 3, 4, 8 ] 3 4
});
